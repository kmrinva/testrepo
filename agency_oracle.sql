CREATE TABLE  agency (
  agencyid number,
  agencycode VARCHAR2(10) ,
  agencyname VARCHAR2(2000) ,
  agencytype VARCHAR2(20)  
  epacode VARCHAR2(20) ,
  biacode VARCHAR2(10) ,
  description VARCHAR2(2000) ,
  createby VARCHAR2(2000) ,
  createdate DATETIME ,
  updateby VARCHAR2(2000) ,
  updatedate DATETIME ,
  deleteind CHAR(1) 
);